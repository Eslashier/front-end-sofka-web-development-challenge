function reducer(state, action){
    switch(action.type){
        case 'get-lists':
            return state
        case 'add-list':
            return state
        case 'add-task':
            return state
        case 'remove-list':
            return state
        case 'remove-task':
            return state
        case 'update-task':
            return state
    }
}

export default reducer